CREATE TABLE CHOFER (
        ID_CHOFER INTEGER PRIMARY KEY,
        CNOMBRE VARCHAR(64) NOT NULL,
        CAPP VARCHAR(64) NOT NULL,
        CAPM VARCHAR(64) NOT NULL,
        CNO_CUENTA VARCHAR(64) UNIQUE NOT NULL,
        CNO_ID VARCHAR(64) UNIQUE NOT NULL, -- numero de identificacion en licencia de calificacion 
        CFECHA_NAC DATE NOT NULL,
        CCONTRASENIA VARCHAR(128) NOT NULL
);

CREATE TABLE AUTOMOVIL (
       ID_AUTOMOVIL INTEGER PRIMARY KEY,
       NO_IDENTIFICACION INTEGER UNIQUE NOT NULL, 
       MODELO VARCHAR(64) NOT NULL,
       PLACAS VARCHAR(8) UNIQUE NOT NULL,
       COLOR VARCHAR(32) NOT NULL,
       ID_CHOFER INTEGER REFERENCES CHOFER (ID_CHOFER) NOT NULL
);

CREATE TABLE RUTA (
       ID_RUTA INTEGER PRIMARY KEY,
       MAPA VARCHAR(256) NOT NULL,
       DESCRIPCION VARCHAR(120),
       ACTIVA BOOLEAN NOT NULL,
       FECHA_CREACION DATE NOT NULL,
       ID_AUTO INTEGER REFERENCES AUTOMOVIL (ID_AUTOMOVIL) NOT NULL
);

CREATE TABLE HORARIO (
       ID_HORARIO INTEGER PRIMARY KEY,
       LUNES INTEGER, 
       MARTES INTEGER,
       MIERCOLES INTEGER,
       JUEVES INTEGER,
       VIERNES INTEGER,
       SABADO INTEGER,
       ID_RUTA INTEGER REFERENCES RUTA (ID_RUTA) NOT NULL
);

CREATE TABLE PASAJERO (
       ID_PASAJERO INTEGER PRIMARY KEY,
       PNOMBRE VARCHAR(64) NOT NULL,
       PAPP VARCHAR(64) NOT NULL,
       PAPM VARCHAR(64) NOT NULL,
       PCORREO VARCHAR(128) UNIQUE NOT NULL,
       PNO_CUENTA VARCHAR(9) UNIQUE NOT NULL,
       PFECHA_NAC DATE NOT NULL,
       PCONTRASENIA VARCHAR(128) NOT NULL
);

CREATE TABLE PASAJERO_RUTA (
        DIA INTEGER NOT NULL,
        ID_PASAJERO INTEGER REFERENCES PASAJERO (ID_PASAJERO) NOT NULL,
        ID_RUTA INTEGER REFERENCES RUTA (ID_RUTA) NOT NULL,
        PRIMARY KEY (ID_PASAJERO, ID_RUTA)
);

CREATE TABLE PERFIL_PASAJERO (
        ID_PPASAJERO INTEGER PRIMARY KEY,
        FOTO VARCHAR(256),
        SOBRE_MI VARCHAR(256),
        ESTADO BOOLEAN,
        ID_PASAJERO INTEGER REFERENCES PASAJERO (ID_PASAJERO) NOT NULL
);

CREATE TABLE PERFIL_CHOFER (
        ID_PCHOFER INTEGER PRIMARY KEY,
        FOTO VARCHAR(256),
        SOBRE_MI VARCHAR(256),
        ESTADO BOOLEAN,
        ID_CHOFER INTEGER REFERENCES CHOFER (ID_CHOFER) NOT NULL
);
